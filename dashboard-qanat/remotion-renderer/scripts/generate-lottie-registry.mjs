/**
 * Scans lottie_assets/ and generates lottieRegistry.generated.ts
 * Run: node scripts/generate-lottie-registry.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ASSETS_DIR = path.join(__dirname, "../src/overlays/lottie_assets");
const OUT_FILE = path.join(__dirname, "../src/overlays/lottieRegistry.generated.ts");

const CATEGORY_PRIORITY = {
  nature: 0,
  edu: 1,
  life: 2,
  tech: 3,
  biz: 4,
  sports: 5,
  interact: 6,
  social: 7,
  arrow: 8,
  ui: 9,
  misc: 10,
};

function parseFilename(filename) {
  const standard = filename.match(/^lottie_([a-z]+)_([a-z0-9]+)_(\d+)\.json$/);
  if (standard) {
    return {
      category: standard[1],
      key: standard[2],
      variant: Number(standard[3]),
      filename,
    };
  }
  const simple = filename.match(/^([a-z0-9_]+)\.json$/);
  if (simple) {
    const key = simple[1].replace(/^weather_/, "");
    return { category: "misc", key, variant: 1, filename };
  }
  return null;
}

function pickCanonical(entries) {
  return [...entries].sort((a, b) => {
    const pa = CATEGORY_PRIORITY[a.category] ?? 99;
    const pb = CATEGORY_PRIORITY[b.category] ?? 99;
    if (pa !== pb) return pa - pb;
    return a.variant - b.variant;
  })[0];
}

const files = fs.readdirSync(ASSETS_DIR).filter((f) => f.endsWith(".json"));
const byKey = new Map();

for (const file of files) {
  const parsed = parseFilename(file);
  if (!parsed) continue;
  if (!byKey.has(parsed.key)) byKey.set(parsed.key, []);
  byKey.get(parsed.key).push(parsed);
}

const keys = [...byKey.keys()].sort();
const imports = [];
const mapEntries = [];

for (const key of keys) {
  const canonical = pickCanonical(byKey.get(key));
  const varName = `lottie_${key.replace(/[^a-z0-9]/gi, "_")}`;
  imports.push(`import ${varName} from "./lottie_assets/${canonical.filename}";`);
  mapEntries.push(`  ${JSON.stringify(key)}: ${varName},`);
}

const content = `/* eslint-disable */
// AUTO-GENERATED by scripts/generate-lottie-registry.mjs — do not edit manually
// ${keys.length} ícones · ${files.length} arquivos em lottie_assets/

${imports.join("\n")}

export const LOTTIE_REGISTRY: Record<string, object> = {
${mapEntries.join("\n")}
};

export const LOTTIE_KEYS = ${JSON.stringify(keys)} as const;
export type LottieRegistryKey = typeof LOTTIE_KEYS[number];
`;

fs.writeFileSync(OUT_FILE, content, "utf8");
console.log(`[lottie-registry] ${keys.length} keys → ${path.relative(process.cwd(), OUT_FILE)}`);